def normalize_search_text(text: str) -> str:
    normalized = text.lower().replace("ё", "е")
    normalized = re.sub(r">>\d+", " ", normalized)
    normalized = re.sub(r"[^0-9a-zа-я]+", " ", normalized)
    return " ".join(normalized.split())


def tokenize_search_query(query: str) -> list[str]:
    normalized = normalize_search_text(query)
    tokens = []
    for token in normalized.split():
        if len(token) < 3:
            continue
        if token in RUSSIAN_STOPWORDS:
            continue
        tokens.append(token)
    return tokens


def expand_query_token(token: str) -> tuple[str, ...]:
    normalized = normalize_search_text(token)
    if not normalized:
        return ()
    variants = [normalized]
    variants.extend(QUERY_SYNONYMS.get(normalized, ()))
    return tuple(dict.fromkeys(variants))


def expand_query_tokens(tokens: list[str]) -> list[tuple[str, ...]]:
    return [expand_query_token(token) for token in tokens]
