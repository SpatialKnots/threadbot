from __future__ import annotations

from app.search.normalization import normalize_search_text


TAG_RULES: dict[str, tuple[str, ...]] = {
    "батя": ("батя", "батю", "батей", "отец", "отца", "папа", "папу", "папаня", "пахан"),
    "семья": ("мама", "мать", "отец", "батя", "папа", "сын", "дочь", "ребенок", "семья"),
    "работа": ("работа", "работы", "смена", "смены", "смену", "завод", "офис", "начальник", "коллега", "вахта"),
    "общага": ("общага", "общагу", "общаге", "общежитие", "сосед", "соседи", "комната"),
    "алкоголь": ("пиво", "водка", "бухать", "бухой", "пьяный", "синий", "алкоголь"),
    "отношения": ("девушка", "парень", "бывшая", "бывший", "любовь", "свидание", "отношения"),
    "учеба": ("школа", "универ", "пара", "экзамен", "студент", "учеба"),
    "еда": ("кухня", "кухне", "еда", "жрать", "столовая", "шаурма"),
    "скуф": ("скуф", "мужик", "дядя"),
}


def extract_tags(text: str) -> list[str]:
    normalized = f" {normalize_search_text(text)} "
    found: list[str] = []
    for tag, keywords in TAG_RULES.items():
        if any(f" {normalize_search_text(keyword)} " in normalized for keyword in keywords):
            found.append(tag)
    return sorted(set(found))
